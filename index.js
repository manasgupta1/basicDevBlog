  const cardPerPage = 12;
  let currentPage = 1;
  const totalCards = data.length;
  const totalPages = Math.ceil(totalCards / cardPerPage);

  function displayCards() {
    const startIndex = (currentPage - 1) * currentPage;
    const endIndex = startIndex + cardPerPage;
    const visibleCards = data.slice(startIndex, endIndex);

    mainBody.innerHTML = "";
    visibleCards.forEach((element) => {
      console.log(element);
      let cardDiv = document.createElement("div");
      let imgElement = document.createElement("img");
      imgElement.src = `${element.images.thumbnail}`;
      imgElement.alt = "Image Alt Text";
      let cardBodyDiv = document.createElement("div");
      let h5Element = document.createElement("h5");
      h5Element.textContent = element.title;
      let pElement = document.createElement("p");
      pElement.textContent = element.snippet;
      let aElement = document.createElement("a");
      aElement.href = element.newsUrl;
      aElement.textContent = "Read More";
      cardDiv.classList.add("card");
      imgElement.classList.add("card-img");
      cardBodyDiv.classList.add("card-body");
      h5Element.classList.add("card-title");
      pElement.classList.add("card-text");
      aElement.classList.add("btn");
      cardDiv.appendChild(imgElement);
      cardDiv.appendChild(cardBodyDiv);
      cardBodyDiv.appendChild(h5Element);
      cardBodyDiv.appendChild(pElement);
      cardBodyDiv.appendChild(aElement);
      mainBody.appendChild(cardDiv);
    });
  }

  function updatePagination() {
    document.getElementById("currentPage").textContent = currentPage;
  }

  $("#nextPage").click(function () {
    if (currentPage < totalPages) {
      currentPage++;
      displayCards();
      updatePagination();
    }
  });

  $("#prevPage").click(function () {
    if (currentPage > 1) {
      currentPage--;
      displayCards();
      updatePagination();
    }
  });

  displayCards();
  updatePagination();
